"""Golden fidelity tests (issue #83, Phase 1): the stdlib-only shim must produce
the SAME structural spec as the real Airflow-based compiler for every example.

The golden files in ``golden/`` were generated by running the real
``leoflow_parser compile`` (with apache-airflow installed) on each example and
stripping the pass-through ``image`` field. These tests run only the shim — no
Airflow needed — so they gate fidelity in CI.

Regenerate goldens (needs an Airflow venv):
    for d in examples/*/; do leoflow_parser compile --source $d/dag.py \
        --config $d/leoflow.yaml --output /tmp/x.json --image i:v --dag-version v1; done
"""
from __future__ import annotations

import json
from pathlib import Path

import pytest

import extract

HERE = Path(__file__).resolve().parent
REPO = HERE.parents[1]
GOLDEN = sorted((HERE / "golden").glob("*.json"))


# The fields the shim derives from the DAG itself. The rest of the real spec
# (owner, schema_version, dag_version, image) is config/CLI pass-through added by
# the compiler wrapper, not parsed from the DAG, so it is out of the shim's scope.
STRUCTURAL_KEYS = ("dag_id", "schedule", "tags", "tasks")


def _structural(spec: dict) -> dict:
    return {k: spec[k] for k in STRUCTURAL_KEYS if k in spec}


@pytest.mark.parametrize("golden_file", GOLDEN, ids=lambda p: p.stem)
def test_shim_matches_real_compiler(golden_file):
    name = golden_file.stem
    example = REPO / "examples" / name
    want = _structural(json.loads(golden_file.read_text()))
    got = _structural(extract.compile_dag(
        str(example / "dag.py"),
        config=str(example / "leoflow.yaml"),
    ))
    # Canonical compare (key order independent; task lists are sorted in both).
    assert json.dumps(got, sort_keys=True) == json.dumps(want, sort_keys=True), (
        f"\n{name}: shim structure differs from the real compiler.\n"
        f"shim:   {json.dumps(got, sort_keys=True)}\n"
        f"golden: {json.dumps(want, sort_keys=True)}"
    )
